package project.dto;

public class CgoodDto {
	private String cgood;
	private String cg_code;
	private String cg_id;
	private String cg_data;
	private String y_code;
	
	public String getY_code() {
		return y_code;
	}
	public void setY_code(String y_code) {
		this.y_code = y_code;
	}
	public String getCgood() {
		return cgood;
	}
	public void setCgood(String cgood) {
		this.cgood = cgood;
	}
	public String getCg_code() {
		return cg_code;
	}
	public void setCg_code(String cg_code) {
		this.cg_code = cg_code;
	}
	public String getCg_id() {
		return cg_id;
	}
	public void setCg_id(String cg_id) {
		this.cg_id = cg_id;
	}
	public String getCg_data() {
		return cg_data;
	}
	public void setCg_data(String cg_data) {
		this.cg_data = cg_data;
	}
	
}
